"""
    obtain_symmetry_related_hoppings(
        Rs::AbstractVector{V}, 
        br‚āź::NewBandRep{D}, 
        brŠĶ¶::NewBandRep{D};
        diagonal_block::Bool = true,
        reverse_hop::Bool = false,
        nonhermitian::Bool = false
    ) --> Vector{HoppingOrbit{D}}

Compute the symmetry-related hopping terms from the points in the WP of `br‚āź` to the 
WP of `brŠĶ¶`, displaced by a set of primitive lattice-vector representatives `Rs`.

## Implementation
1. Take a point `a` in the WP of `br‚āź` and a point `b` in the WP of `brŠĶ¶`. 
    Compute the displacement vector `őī = b + R - a`, where `R ‚ąą Rs`, giving the displacement
    vector from `a` (created site) to `b + R` (annihilated site): i.e., `őī` points opposite
    to the hopping direction.
2. If `őī ‚ąą representatives`, add `őī => (a, b, R)` to the list of hoppings 
    for that representative and continue. Otherwise, search all representatives for one 
    whose list of hoppings contains `őī => (a, b, R)`. If none is found, add `őī` as a new 
    representative and add `őī => (a, b, R)` to its list of hoppings.
3. Take `g ‚ąą generators`, compute `őī' = g őī` and `(a', b', R') = (g a, g b, g R)`, 
    and repeat step 2.
4. Repeat steps 1‚Äď3 for all pairs of points in the WPs of `br‚āź` and `brŠĶ¶`.

Additionally, if we are considering a diagonal block (which must be Hermitian or anti-Hermitian),
we check whether orbits that relate `őī` and `-őī` are present; if not, we add them.
"""
function obtain_symmetry_related_hoppings(
    Rs::AbstractVector{V}, # must be specified in the primitive basis
    br‚āź::NewBandRep{D},
    brŠĶ¶::NewBandRep{D};
    diagonal_block::Bool = true, # whether to manually add "reversed" hoppings (if true),
    reverse_hop::Bool = false, # if hopping actually goes from a+R‚Üíb rather than a‚Üíb+R
                               # (used in NONHERMITIAN case, for lower-triangular blocks)
                               # practically just corresponds to flipping signs of `Rs`
    nonhermitian::Bool = false # whether to assume the model is NONHERMITIAN: if false,
                               # we assume HERMITIAN or ANTIHERMITIAN and add & merge
                               # reversed hoppings explicitly; if true (NONHERMITIAN), we
                               # augment every R by -R if diagonal_block is true, to ensure
                               # that we include both "sides" of terms that _would be_
                               # related by Hermitcity (but don't merge them)
) where {V <: Union{AbstractVector{<:Integer}, RVec{D}}} where {D}
    sgnum = num(br‚āź)
    num(brŠĶ¶) == sgnum ||
        error("both band representations must belong to the same space group")
    br‚āź.timereversal == brŠĶ¶.timereversal ||
        error("input band representations must have identical time-reversal symmetry")

    # we only want to include the Wyckoff positions in the primitive cell - but the default
    # listings from `spacegroup` include operations that are "centering translations";
    # fortunately, the orbit returned for a `NewBandRep` do not include these redundant
    # operations - but is still specified in a conventional basis. So, below, we remove
    # redundant operations from the space group, and also change both the operations and the
    # positions from a conventional to a primitive basis
    cntr = centering(sgnum, D)
    ops = primitivize(spacegroup(sgnum, Val{D}()))
    wps‚āź = primitivize.(orbit(group(br‚āź)), cntr)
    wpsŠĶ¶ = primitivize.(orbit(group(brŠĶ¶)), cntr)

    # we have defined a structure `HoppingOrbit` to gather the information. It is 
    # structured as:
    # 1. `HoppingOrbit.representatives` will store a representative hopping vector őī
    # 2. `HoppingOrbit.orbit` will store the full orbit of symmetry related hopping 
    #   vector generated by őī: {őīŠĶĘ}
    # 3. `HoppingOrbit.hoppings` will store the real coordinates `(a,b,R)` of each 
    #   hopping term associated to each `őīŠĶĘ`. Note that maybe several `(a,b,R)` could be 
    #   associated to the same `őīŠĶĘ`

    # in the non-Hermitian case, we augment Rs by -Rs if we are in a diagonal block, to
    # ensure we return both "sides" of terms that _would be_ related by Hermiticity;
    # this effectuates a functionality similar to having `reverse_hop` equal to both true
    # _and_ false
    # the reason we need to do this explicitly in the nonhermitian case, is that without
    # hermiticity, we are no longer guaranteed that a hop `a ‚Üí b+R` links to its conjugate
    # hop `b ‚Üí a-R`; but a user would usually expect to see both "sides" of such a paired
    # term appear in a returned listing (even though they end up as separate terms); so we
    # augment to ensure this (and the augmenting is only necessary in the diagonal_block,
    # as `reverse_hop` is passed as `true` by `tb_hamiltonian` for non-diagonal blocks)
    if diagonal_block && nonhermitian
        reverse_hop && error("unexpected input combination: `reverse_hop = true`, `nonhermitian = true`, & `diagonal_block = true`")
        Rs = _augment_with_negative_counterparts(Rs)
    end

    h_orbits = HoppingOrbit{D}[]
    for R in Rs
        reverse_hop && (R = -R) # treat as hopping from a+R‚Üíb (equivalently, from a‚Üíb-R)
        R = RVec{D}(R) # change the type of R for type consistency
        for (q‚āź, qŠĶ¶) in Iterators.product(wps‚āź, wpsŠĶ¶)
            q‚āź = parent(q‚āź) # work with RVec directly rather than Wyckoff Position
            qŠĶ¶ = parent(qŠĶ¶)
            őī = qŠĶ¶ + R - q‚āź # potential representative in next element of `h_orbits`
            maybe_add_hoppings!(h_orbits, őī, q‚āź, qŠĶ¶, R, ops)
        end
    end

    # hermiticity/anti-hermiticity could link orbits that spatial symmetries might not have
    # already linked: in particular, if we are in a "diagonal block" of the Hamiltonian,
    # then, for every hopping vector `őī`, we must have a "reversed" counterpart `-őī` in the
    # presence of hermiticity or anti-hermiticity; but those two vectors might have fallen
    # in distinct orbits up this point - if so, we now merge them
    if diagonal_block && !nonhermitian
        add_reversed_orbits!(h_orbits)
    end

    return h_orbits
end

"""
    maybe_add_hoppings!(h_orbits, őī, q‚āź, qŠĶ¶, R, ops) --> Vector{HoppingOrbit{D}}

Checks whether a hopping term `őī` is already in the list of representatives. If not, adds it
and its symmetry-related partners. If it is, only the symmetry-related partners are added.
"""
function maybe_add_hoppings!(
    h_orbits,
    őī,
    q‚āź,
    qŠĶ¶,
    R,
    ops::AbstractVector{SymOperation{D}},
) where {D}
    őī_idx = findfirst(h_orbits) do h_orbit
        # check if őī is in existing h_orbits
        isapproxin(őī, orbit(h_orbit), nothing, false; atol = VEC_CMP_ATOL)
    end
    if isnothing(őī_idx)
        # if it wasn't already in `h_orbits`, we add it and its symmetry-related partners
        őī_orbit = HoppingOrbit{D}(őī, [őī], [[(q‚āź, qŠĶ¶, R)]])
        push!(h_orbits, őī_orbit)
    else # if it was already in `h_orbits`, we only add its symmetry-related partners
        őī_orbit = h_orbits[őī_idx]
    end
    _maybe_add_hoppings!(őī_orbit, őī, q‚āź, qŠĶ¶, R, ops)
end

"""
Computes and adds the symmetry related partners of a hopping term `őī` to the `őī_orbit`.

!!! warning
    This function is an internal helper function for `maybe_add_hoppings!` and
    is not part of the public API.
"""
function _maybe_add_hoppings!(
    őī_orbit,
    őī,
    q‚āź,
    qŠĶ¶,
    R,
    ops::AbstractVector{SymOperation{D}},
) where {D}
    for g in ops
        _q‚āź‚Ä≤ = g * q‚āź
        _qŠĶ¶‚Ä≤ = g * (qŠĶ¶ + R)
        # _q‚āź‚Ä≤ and _qŠĶ¶‚Ä≤ can include a translation part that is a multiple of the lattice
        # translation, so we reduce it to a unit range & keep the translation part separate
        q‚āź‚Ä≤ = RVec(reduce_translation_to_unitrange(constant(_q‚āź‚Ä≤)), free(_q‚āź‚Ä≤))
        qŠĶ¶‚Ä≤ = RVec(reduce_translation_to_unitrange(constant(_qŠĶ¶‚Ä≤)), free(_qŠĶ¶‚Ä≤))
        d‚āź = _q‚āź‚Ä≤ - q‚āź‚Ä≤ # possible lattice translation part of _q‚āź‚Ä≤
        dŠĶ¶ = _qŠĶ¶‚Ä≤ - qŠĶ¶‚Ä≤ # possible lattice translation part of _qŠĶ¶‚Ä≤

        R‚Ä≤ = dŠĶ¶ - d‚āź # total lattice translation part of the symmetry-related hopping term őī'
        g_rotation = SymOperation(rotation(g)) # rotation-only part of g
        őī‚Ä≤ = g_rotation * őī # potential symmetry related partner of `őī` to add to `őī_orbit`

        # several sanity checks:
        #   1. R¬ī is a lattice translation, i.e., it is integer
        #   2. R¬ī doesn't have any free parameters
        #   3. őī‚Ä≤ = qŠĶ¶‚Ä≤ + R‚Ä≤ - q‚āź‚Ä≤
        all(RŠĶĘ‚Ä≤ -> abs(RŠĶĘ‚Ä≤ - round(RŠĶĘ‚Ä≤)) < VEC_CMP_ATOL, constant(R‚Ä≤)) ||
            error("arrived at non-integer lattice translation R‚Ä≤: should be impossible")
        isspecial(R‚Ä≤) || error(
            "arrived at non-special (nonzero free parameters) lattice translation R‚Ä≤: should be impossible",
        )
        isapprox(őī‚Ä≤, qŠĶ¶‚Ä≤ + R‚Ä≤ - q‚āź‚Ä≤, nothing, false; atol = VEC_CMP_ATOL) ||
            error("őī‚Ä≤ ‚Č† qŠĶ¶‚Ä≤ + R‚Ä≤ - q‚āź‚Ä≤")

        idx_in_orbit = findfirst(orbit(őī_orbit)) do őī‚Ä≤‚Ä≤
            isapprox(őī‚Ä≤, őī‚Ä≤‚Ä≤, nothing, false; atol = VEC_CMP_ATOL)
        end
        if isnothing(idx_in_orbit)
            # őī‚Ä≤ is not already included in `orbit(őī_orbit)`
            push!(orbit(őī_orbit), őī‚Ä≤)
            push!(őī_orbit.hoppings, [(q‚āź‚Ä≤, qŠĶ¶‚Ä≤, R‚Ä≤)])
        else
            # őī‚Ä≤ already in `orbit(őī_orbit)` but hopping term might not be: evaluate
            # `(q‚āź‚Ä≤, qŠĶ¶‚Ä≤, R‚Ä≤) ‚ąČ őī_orbit.hoppings[idx_in_orbit]`, w/ approx. comparison
            bool = !any(őī_orbit.hoppings[idx_in_orbit]) do (q‚āź‚Ä≤‚Ä≤, qŠĶ¶‚Ä≤‚Ä≤, R‚Ä≤‚Ä≤)
                    isapprox(q‚āź‚Ä≤, q‚āź‚Ä≤‚Ä≤, nothing, false; atol = VEC_CMP_ATOL) &&
                    isapprox(qŠĶ¶‚Ä≤, qŠĶ¶‚Ä≤‚Ä≤, nothing, false; atol = VEC_CMP_ATOL) &&
                    isapprox(R‚Ä≤, R‚Ä≤‚Ä≤, nothing, false; atol = VEC_CMP_ATOL)
                end
            if bool
                push!(őī_orbit.hoppings[idx_in_orbit], (q‚āź‚Ä≤, qŠĶ¶‚Ä≤, R‚Ä≤))
            end
        end
    end
    return őī_orbit
end

"""
    add_reversed_orbits!(h_orbits::Vector{HoppingOrbit{D}}) where {D}

Adds the reversed hopping terms to the hopping orbits in `h_orbits`. The reversed
hopping terms are added to the orbit of the hopping term they are related to; if they are
already present in another orbit, the two orbits are merged.

It is assumed that this functionality will only be called for diagonal blocks.
"""
function add_reversed_orbits!(
    h_orbits::Vector{HoppingOrbit{D}}
) where {D}
    # for any orbit that contains a hopping vector `őī`, we check if its reversed
    # hopping vector `-őī` is also in the orbit; if not, we check if it is in any other
    # orbit to merge them, and, if not, we add it manually to the orbit

    # below, we assume that if, for some őī, no counterpart is found, then no other őī in the
    # orbit has a counterpart, so we can just add it manually

    # determine if `h_orbit`s should be modified
    for (n, h_orbit) in enumerate(h_orbits)
        őīs = orbit(h_orbit) # őīs in the current orbit

        # check whether the orbit is already "good" (i.e., all őīs have a -őī counterpart)
        if all(őī -> isapproxin(-őī, őīs, nothing, false; atol = VEC_CMP_ATOL), őīs)
            # all őīs have a -őī counterpart in the orbit: nominally, the orbit is good as-is
            # there is a corner-case, however, since we must also ensure that for every őī
            # hop a ‚Üí b + R, the hermitian-partner hop b ‚Üí a - R exists at -őī; this is not
            # guaranteed automatically, as -őī may have entered the orbit through a symmetry
            # operation that does not necessarily correspond to this "hermitian-partner" 
            # ("reversed hop") relationship; so we check & add any missing "reversed hops"
            hoppings_original = [copy(hops) for hops in h_orbit.hoppings]
            for (idx, őī) in enumerate(őīs)
                idx‚Ä≤ = something(findfirst(őīs) do őī‚Ä≤
                    isapprox(-őī, őī‚Ä≤, nothing, false; atol = VEC_CMP_ATOL)
                end)
                hops = hoppings_original[idx]
                hops‚Ä≤ = h_orbit.hoppings[idx‚Ä≤]
                for hop in hops
                    (q‚āź, qŠĶ¶, R) = hop
                    rev_hop = (qŠĶ¶, q‚āź, -R) # reverse hop
                    rev_q‚āź, rev_qŠĶ¶, rev_R = rev_hop
                    has_counterpart = any(hops‚Ä≤) do hop‚Ä≤
                        q‚āź‚Ä≤, qŠĶ¶‚Ä≤, R‚Ä≤ = hop‚Ä≤
                        (isapprox(rev_q‚āź, q‚āź‚Ä≤, nothing, false; atol = VEC_CMP_ATOL) &&
                         isapprox(rev_qŠĶ¶, qŠĶ¶‚Ä≤, nothing, false; atol = VEC_CMP_ATOL) &&
                         isapprox(rev_R,  R‚Ä≤,  nothing, false; atol = VEC_CMP_ATOL))
                    end
                    has_counterpart || push!(hops‚Ä≤, rev_hop)
                end
            end
            continue # the orbit is now properly closed: nothing to merge & we continue
        end

        merge_idx = findfirst(@view h_orbits[n+1:end]) do h_orbit‚Ä≤
            isapproxin(-őīs[1], orbit(h_orbit‚Ä≤), nothing, false; atol = VEC_CMP_ATOL)
        end
        if !isnothing(merge_idx)
            # at least one őī has a -őī counterpart in another orbit - we need to merge them
            # NB: We assume that if one őī has a -őī counterpart in another orbit, then all
            #     őīs in the orbit have a -őī counterpart in the same orbit, so we merge them
            n‚Ä≤ = something(merge_idx) + n
            h_orbit‚Ä≤ = h_orbits[n‚Ä≤]

            # merge the orbits
            append!(h_orbit.orbit, h_orbit‚Ä≤.orbit)
            append!(h_orbit.hoppings, h_orbit‚Ä≤.hoppings)

            # remove the merged orbit
            deleteat!(h_orbits, n‚Ä≤)
            continue
        end
        # we now know that at least some őī doesn't have a -őī counterpart; we assume that
        # this implies that none of the őīs have a -őī counterpart, so we need to add them all
        @assert all(őī -> !isapproxin(-őī, őīs, nothing, false; atol = VEC_CMP_ATOL), őīs)

        # first append the new őīs into the orbit
        append!(őīs, -őīs)

        # add the "reversed" hopping terms: i.e., for every a ‚Üí b + R, add b + R ‚Üí a 
        # => -őī = a - (b + R) = a - b - R = b - 2b - R -a + 2a = b + (2a - 2b - R) - a = b + R' - a
        hoppings = h_orbit.hoppings
        hoppings‚Ä≤ = map(hoppings) do hops
            map(hops) do (q‚āź, qŠĶ¶, R)
                (qŠĶ¶, q‚āź, -R) # reverse the hopping term
            end
        end
        append!(hoppings, hoppings‚Ä≤)
    end
    return h_orbits
end

# for NONHERMITIAN models, hermiticity no longer links a hop `a ‚Üí b+R` to its conjugate
# hop `b ‚Üí a-R`; but, in presenting the hopping terms, a user would usually expect to
# see both kinds of hopping families be included if one is - even though they end up as
# separate terms: to ensure this, we add -R to Rs if it is not already in Rs (insert -R
# immediately after R if not already present)
function _augment_with_negative_counterparts(Rs)
    Rs‚Ä≤ = empty!(similar(Rs)) # empty vector with suitable backing size
    for R in Rs
        push!(Rs‚Ä≤, R)
        R_neg = -R
        if !isapproxin(R_neg, Rs; atol = VEC_CMP_ATOL)
            push!(Rs‚Ä≤, R_neg)
        end
    end
    return Rs‚Ä≤
end
