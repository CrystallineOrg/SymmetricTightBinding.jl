# NB: The ordering of `t` is a bit subtle: `t` is a kind of vector-flattened
#        tensor `T`, with the following "indexing convention" for `T`:
#        1. The elements `T[i]` give a vector of hoppings corresponding to the `i`th orbit
#           `őīŠĶĘ = h_orbit.orbit[i]`. 
#        2. The elements `T[i][j]` give a vector of hoppings corresponding to the `j`th
#           possible spatial hopping with displacement `őīŠĶĘ`, associated with
#           `hŠĶĘ‚Īľ = h_orbit.hoppings[i][j]`.
#           This describes hoppings from site an "origin" `hŠĶĘ‚Īľ[1]` to a "destination"
#           `hŠĶĘ‚Īľ[2] + hŠĶĘ‚Īľ[3]` (where `hŠĶĘ‚Īľ[3]` is an integer "R" translation).
#        3. Finally, `T[i][j][k][m]` is a _single_ hopping term from the `k`-th partner
#           function of `hŠĶĘ‚Īľ[1]` site to the `m`-th partner function of `hŠĶĘ‚Īľ[2]` site.
#        If we are considering a diagonal block, i.e., `br‚āź == brŠĶ¶`, then we also have
#        to include the Hermitian or anti-Hermitian counterparts of each hopping term,
#        i.e., for each hopping term from `hŠĶĘ‚Īľ[1]` to `hŠĶĘ‚Īľ[2] + hŠĶĘ‚Īľ[3]`, we also have
#        to include the hopping term from `hŠĶĘ‚Īľ[2]` to `hŠĶĘ‚Īľ[1] - hŠĶĘ‚Īľ[3]`.

# NB: Time-reversal symmetry does NOT imply that if we have a hopping from q to w, we must
#     also have a hopping from w to q. This is a natural first assumption, but a false one.

#     As an example, consider a simple system with two sites, one at [0,0] and another at 
#     [2/3, 1/3]. Let us call `a` an orbital placed at [0,0] and `c` an orbital placed
#     at [2/3, 1/3]. Then, a possible Hamiltonian for first-nearest neighbors is: 
#        `H = \sum_ūĚź§ (őĪ e^{i2ŌÄ ūĚź§¬∑[-2/3, -1/3]} c_ūĚź§‚Ä† a_ūĚź§ ¬Ī h.c.)`
#     where `¬Ī` accounts for hermiticity or anti-hermiticity. Let me prove that this
#     Hamiltonian is TRS iff `őĪ` is real. Assume that the time-reversal operator
#     acts on the operators as: ūĚíĮ c_ūĚź§ ūĚíĮ‚ĀĽ¬Ļ = c_-ūĚź§, ūĚíĮ c_ūĚź§‚Ä† ūĚíĮ‚ĀĽ¬Ļ = c_-ūĚź§‚Ä†; and the same for
#     `a_ūĚź§`. Then, applying the TRS operator to the Hamiltonian, we find:
#        `ūĚíĮ H ūĚíĮ‚ĀĽ¬Ļ = \sum_ūĚź§ (őĪ* e^{-i2ŌÄ ūĚź§¬∑[-2/3, -1/3]} c_-ūĚź§‚Ä† a_-ūĚź§ ¬Ī h.c.)`
#     Changing the summation variable ūĚź§ ‚Üí -ūĚź§, we find:
#        `ūĚíĮ H ūĚíĮ‚ĀĽ¬Ļ = \sum_ūĚź§ (őĪ* e^{i2ŌÄ ūĚź§¬∑[-2/3, -1/3]} c_ūĚź§‚Ä† a_ūĚź§ ¬Ī h.c.)`
#     Then, the only requirement for TRS is that `őĪ = őĪ*`, i.e., `őĪ` is real.

#     This means that, in general, if we have a hopping from q to w, we don't need to
#     have a hopping from w to q.

#     The only terms where we need to consider reversed hoppings are the cases of blocks where
#     hermiticity or anti-hermiticity relates the block to itself: i.e., diagonal blocks. There,
#     if we have a hopping from q to w, we must also have a hopping from w to q, but they both
#     correspond to the same Wyckoff position and site-symmetry irrep, so the former discussion
#     holds.

# ---------------------------------------------------------------------------- #

"""
    OrbitalOrdering(br::NewBandRep{D}) --> OrbitalOrdering{D}

Establishes a canonical, local ordering for the orbitals associated with a band representation
`br`. This is the default ordering used when associating row/column indices in a
tight-binding Hamiltonian block to specific orbitals in the associated band representations.

The canonical orbital ordering is stored in `.ordering`. The `i`th element of `ordering`,
`ordering[i]`, is a `NamedTuple` with two fields:
`wp` and `idx`:
- `wp`: stores a Wyckoff position in the orbit of the Wyckoff position associated to
  `br.wp`.
- `idx`: stores the index of the partner function of the site-symmetry irrep associated to
  `br` at `wp`.

I.e., the `i`th orbital associated with `br` is located at `wp` and transforms as the `idx`th
partner function of the site-symmetry irrep of `br.siteir`.
The total number of orbitals associated to a band representation, and hence the length of
`ordering`, is the product of the site-symmetry irrep dimensionality and the number of sites
in the Wyckoff position orbit.
"""
function OrbitalOrdering(br::NewBandRep{D}) where {D}
    # we only want to include the wyckoff positions in the primitive cell - but the default
    # listings from `spacegroup` include operations that are "centering translations";
    # fortunately, the orbit returned for a `NewBandRep` do not include these redundant
    # operations - but is still specified in a conventional basis. So, below, we remove
    # redundant operations from the space group, and also change both the operations and the
    # positions from a conventional to a primitive basis
    sgnum = num(br)
    cntr = centering(sgnum, D)
    wps = primitivize.(orbit(group(br)), cntr)

    V = length(wps)       # number of Wyckoff positions in orbit
    Q = irdim(br.siteir) # number of orbitals partner functions at each Wyckoff position
    ordering = Vector{@NamedTuple{wp::WyckoffPosition{D}, idx::Int}}(undef, V * Q)
    for i in 1:V
        for k in 1:Q
            ordering[(i-1)*Q+k] = (; wp = wps[i], idx = k)
        end
    end
    return OrbitalOrdering(ordering)
end

"""
    construct_M_matrix(
        h_orbit::HoppingOrbit{D}, br1::NewBandRep{D}, br2::NewBandRep{D},
        [ordering1, ordering2]) 
        --> Array{Int,4}

Construct a set of matrices that encode a Hamiltonian term which resembles the hopping
from EBR `br1` to EBR `br2`.

The encoding is stored as a 4D matrix. Its last two axes correspond to elements of the
Bloch Hamiltonian H(k); its first axis corresponds to `orbit(h_orbit)` and the associated
complex exponentials stored in `v`; and its second axis to the elements of the vector `t`.
That is:

    H‚āõ‚āú(k) = vŠĶĘ(k) MŠĶĘ‚Īľ‚āõ‚āú t‚Īľ

See `devdocs.md` for details.
"""
function construct_M_matrix(
    h_orbit::HoppingOrbit{D},
    br1::NewBandRep{D},
    br2::NewBandRep{D},
    ordering1::OrbitalOrdering{D} = OrbitalOrdering(br1), # canonical orbital orderings for
    ordering2::OrbitalOrdering{D} = OrbitalOrdering(br2),  # `br1` & `br2`, respectively
) where {D}
    V = length(orbit(h_orbit))
    E = length(first(h_orbit.hoppings)) # number of hopping terms per őīŠĶĘ (assumed constant for all i)
    foreach(h_orbit.hoppings) do hops
        if length(hops) ‚Č† E
            error("Unexpectedly had different counts of hoppings across orbit elements")
        end
    end
    Q1, Q2 = irdim(br1.siteir), irdim(br2.siteir)
    Q = Q1 * Q2

    # matrix of matrices that will store the matrix-encoding for each Hamiltonian element
    Mm = zeros(Int, V, V * E * Q, length(ordering1), length(ordering2))

    # fill in the unit-elements of `Mm`
    for őĪ in eachindex(ordering1), ő≤ in eachindex(ordering2)
        (q, i) = ordering1[őĪ]
        (w, j) = ordering2[ő≤]
        q = parent(q)
        w = parent(w)

        # assign a 1 to the correct position of WPs times irrep dimensionality
        for (r, hops) in enumerate(h_orbit.hoppings)
            offset0 = (r - 1) * E * Q
            for (x, hop) in enumerate(hops)
                q‚Ä≤, w‚Ä≤ = hop[1], hop[2]
                if (
                    isapprox(q‚Ä≤, q, nothing, false; atol = VEC_CMP_ATOL) &&
                    isapprox(w‚Ä≤, w, nothing, false; atol = VEC_CMP_ATOL)
                )
                    # an "ordinary" hopping, from `q` to `w`
                    offset1 = (x - 1) * Q
                    c = offset0 + offset1 + (j - 1) * Q1 + i
                    Mm[r, c, őĪ, ő≤] = 1
                end
            end
        end
    end

    return Mm
end

"""
    representation_constraints_matrices(
        Mm::AbstractArray{Int,4}, 
        br‚āź::NewBandRep{D},
        brŠĶ¶::NewBandRep{D}) --> Vector{Array{ComplexF64,4}}

Build the Q matrix for a particular symmetry operation (or, equivalently, a particular matrix
from the site-symmetry representation), acting on the M matrix.

(ŌĀ‚āź‚āź)ŠĶ£‚āõ H‚āõ‚āú (ŌĀŠĶ¶ŠĶ¶‚ĀĽ¬Ļ)‚āú‚āó = (ŌĀ‚āź‚āź)ŠĶ£‚āõ vŠĶĘ MŠĶĘ‚Īľ‚āõ‚āú t‚Īľ (ŌĀŠĶ¶ŠĶ¶‚ĀĽ¬Ļ)‚āú‚āó = vŠĶĘ (ŌĀ‚āź‚āź)ŠĶ£‚āõ MŠĶĘ‚Īľ‚āõ‚āú (ŌĀŠĶ¶ŠĶ¶‚ĀĽ¬Ļ)‚āú‚āó t‚Īľ,

then we can define: QŠĶĘ‚ĪľŠĶ£‚āó = (ŌĀ‚āź‚āź)ŠĶ£‚āõ MŠĶĘ‚Īľ‚āõ‚āú (ŌĀŠĶ¶ŠĶ¶‚ĀĽ¬Ļ)‚āú‚āó
"""
function representation_constraint_matrices(
    Mm::AbstractArray{Int, 4},
    br‚āź::NewBandRep{D},
    brŠĶ¶::NewBandRep{D},
    gens::AbstractVector{SymOperation{D}},
) where {D}
    ŌĀs‚āź‚āź = sgrep_induced_by_siteir_excl_phase.(Ref(br‚āź), gens)
    ŌĀsŠĶ¶ŠĶ¶ = sgrep_induced_by_siteir_excl_phase.(Ref(brŠĶ¶), gens)

    Qs = [similar(Mm, ComplexF64) for _ in eachindex(gens)]
    for (n, (ŌĀ‚āź‚āź, ŌĀŠĶ¶ŠĶ¶)) in enumerate(zip(ŌĀs‚āź‚āź, ŌĀsŠĶ¶ŠĶ¶))
        ŌĀ‚āź‚āź = Matrix(ŌĀ‚āź‚āź) # since `/` doesn't extend to BlockArrays currently
        ŌĀŠĶ¶ŠĶ¶ = Matrix(ŌĀŠĶ¶ŠĶ¶) # for type consistency

        # we have constructed the representation matrices such that gő¶(k) = ŌĀŠĶÄ(g)ő¶(Rk);
        # then, the Hamiltonian will be transformed due to symmetries as
        # H(gūĚź§) = ŌĀ‚āź‚āź(g) H(ūĚź§) ŌĀŠĶ¶ŠĶ¶‚Āļ(g), this can be translated into the numerical 
        # matrices as
        Q = Qs[n]
        for i in axes(Mm, 1), j in axes(Mm, 2)
            Q[i, j, :, :] .= ŌĀ‚āź‚āź * (@view Mm[i, j, :, :]) * ŌĀŠĶ¶ŠĶ¶'
        end
    end

    return Qs
end

"""
    obtain_basis_free_parameters(
        h_orbit::HoppingOrbit{D},
        br‚āź::NewBandRep{D}, 
        brŠĶ¶::NewBandRep{D}, 
       [ordering‚āź = OrbitalOrdering(br‚āź), 
        orderingŠĶ¶ = OrbitalOrdering(brŠĶ¶),
        diagonal_block::Bool = true,
        hermiticity::Hermiticity = HERMITIAN]
        )                            --> Tuple{Array{Int,4}, Vector{Vector{ComplexF64}}}

Obtain the basis of free parameters for the hopping terms between `br‚āź` and `brŠĶ¶` 
associated with the hopping orbit `h_orbit`.

## Note

The presence or absence of time-reversal symmetry is inferred implicitly from `br‚āź` and `brŠĶ¶`.
"""
function obtain_basis_free_parameters(
    h_orbit::HoppingOrbit{D},
    br‚āź::NewBandRep{D},
    brŠĶ¶::NewBandRep{D},
    ordering‚āź::OrbitalOrdering{D} = OrbitalOrdering(br‚āź),
    orderingŠĶ¶::OrbitalOrdering{D} = OrbitalOrdering(brŠĶ¶),
    diagonal_block::Bool = true,
    hermiticity::Hermiticity = HERMITIAN,
) where {D}
    # obtain the needed representations over the generators of each bandrep
    gens‚āź = generators(num(br‚āź), SpaceGroup{D})
    gensŠĶ¶ = generators(num(brŠĶ¶), SpaceGroup{D})
    @assert gens‚āź == gensŠĶ¶ # must be from same space group and in same sorting
    br‚āź.timereversal == brŠĶ¶.timereversal ||
        error("input band representations must have identical time-reversal symmetry")
    timereversal = br‚āź.timereversal

    # cast generators to primitive basis
    cntr = centering(num(br‚āź), D)
    gens = cntr ‚ąą ('P', 'p') ? gens‚āź : primitivize.(gens‚āź, cntr)

    # encode Hamiltonian as a coefficient matrix sandwiched by exponentials & hopping ampl.
    Mm = construct_M_matrix(h_orbit, br‚āź, brŠĶ¶, ordering‚āź, orderingŠĶ¶)

    # actual computation
    t‚āźŠĶ¶_basis_reim = _obtain_basis_free_parameters(
        h_orbit,
        br‚āź,
        brŠĶ¶,
        ordering‚āź,
        orderingŠĶ¶,
        Mm,
        gens,
        timereversal,
        diagonal_block,
        hermiticity,
    )

    return Mm, t‚āźŠĶ¶_basis_reim
end

function _obtain_basis_free_parameters(
    h_orbit::HoppingOrbit{D},
    br‚āź::NewBandRep{D},
    brŠĶ¶::NewBandRep{D},
    ordering‚āź::OrbitalOrdering{D},
    orderingŠĶ¶::OrbitalOrdering{D},
    Mm::Array{Int, 4},
    gens::AbstractVector{SymOperation{D}},
    timereversal::Bool,
    diagonal_block::Bool,
    hermiticity::Hermiticity,
) where {D}
    # encode representation constraints on H‚āźŠĶ¶
    Qs = representation_constraint_matrices(Mm, br‚āź, brŠĶ¶, gens)

    # encode reciprocal-rotation constraints on H‚āźŠĶ¶
    Zs = reciprocal_constraints_matrices(Mm, gens, h_orbit)

    # build an aggregate constraint matrix, over all generators, acting on the hopping
    # coefficient vector t‚āźŠĶ¶ associated with h_orbit
    constraints = _aggregate_constraints(Qs, Zs)
    t‚āźŠĶ¶_basis_matrix = _retrying_nullspace(constraints; atol = NULLSPACE_ATOL_DEFAULT)

    # at this point, the coefficient-space spanning `t‚āźŠĶ¶_basis` is implicitly complex; this
    # is conceptually not too nice for a Hamiltonian, and we'd like to work with strictly
    # real coefficients. To that end, we "split" the possible products of such complex
    # coefficients with a complex basis vector into two vectors, where the lower half of
    # each represents the imaginary part of the product. The benefit of this is that it
    # becomes possible to take the complex conjugate of each element "symbolically" by
    # simply multiplying with [1 0; 0 -1] (block matrix). We need to do this anyway if we
    # we have `timereversal = true`, but it's nice to just have a shared representation
    # of the basis vectors, so we do it here as well.

    # If the set of basis vectors obtained from symmetry constraints was empty, there's no
    # point in continuing: we then return early
    isempty(t‚āźŠĶ¶_basis_matrix) && return Vector{Vector{Float64}}()

    # Details: we split each potentially complex "basis" vector `t = t‚āźŠĶ¶_basis[i]` into
    # two real vectors `x` and `y`, such that the _real_ span of `x` and `y` is equivalent
    # to the complex span of `t`; the key is that we must interpret the lower halfs of `x`
    # and `y` as representing imaginary numbers. Crucially, this must be kept in mind
    # everywhere that we use a coefficient vector. In particular, we now must always
    # interpret `Mm` as acting like `[Mm Mm]` when meeting a "doubled" basis vector of this
    # kind.
    # Example: we have `t‚āĀ = [im,0]`, `t‚āā = [1,im]`, and `t‚āźŠĶ¶_basis = [t‚āĀ,t‚āā]`, then:
    #    ```
    #    julia> t‚āźŠĶ¶_basis_split = split_complex.(t‚āźŠĶ¶_basis)
    #    2-element Vector{Matrix{Int64}}:
    #     [0 -1; 0 0; 1 0; 0 0]
    #     [1 0; 0 -1; 0 1; 1 0]
    #
    #    julia> t‚āźŠĶ¶_basis_split_matrix = reduce(hcat, t‚āźŠĶ¶_basis_split)
    #    4√ó4 Matrix{Int64}:
    #     0  -1  1   0
    #     0   0  0  -1
    #     1   0  0   1
    #     0   0  1   0
    #    ```
    # See `split_complex` for more details on the real/imaginary splitting.
    N = 2size(Mm, 2) # number of elements per `t`-vector, when doubled
    t‚āźŠĶ¶_basis_reim = split_complex.(eachcol(t‚āźŠĶ¶_basis_matrix))
    t‚āźŠĶ¶_basis_reim_matrix =
        reduce(hcat, t‚āźŠĶ¶_basis_reim; init = Matrix{Float64}(undef, N, 0))

    ## ----------------------------------------------------------------------------------- #
    if timereversal # "add" and "intersect" the associated TRS constraints
        # now we construct the TRS constraints, and then intersect the allowable basis terms
        # of these constraints with those corresponding to the previously computed basis
        t‚āźŠĶ¶_basis_tr_reim_matrix =
            obtain_basis_free_parameters_TRS(h_orbit, br‚āź, brŠĶ¶, ordering‚āź, orderingŠĶ¶, Mm)
        t‚āźŠĶ¶_basis_reim =
            zassenhaus_intersection(t‚āźŠĶ¶_basis_reim_matrix, t‚āźŠĶ¶_basis_tr_reim_matrix)
        t‚āźŠĶ¶_basis_reim_matrix =
            reduce(hcat, t‚āźŠĶ¶_basis_reim; init = Matrix{Float64}(undef, N, 0))
    end
    isempty(t‚āźŠĶ¶_basis_reim_matrix) && return Vector{Vector{Float64}}()

    # ------------------------------------------------------------------------------------ #
    # "add" and "intersect" the associated hermiticity constraints if this is a diagonal block
    if diagonal_block && hermiticity ‚Č† NONHERMITIAN
        antihermitian = hermiticity === ANTIHERMITIAN
        t‚āźŠĶ¶_basis_herm_reim_matrix = obtain_basis_free_parameters_hermiticity(
            h_orbit,
            br‚āź,
            brŠĶ¶,
            antihermitian,
            ordering‚āź,
            orderingŠĶ¶,
            Mm,
        )

        t‚āźŠĶ¶_basis_reim = zassenhaus_intersection(
            t‚āźŠĶ¶_basis_reim_matrix, t‚āźŠĶ¶_basis_herm_reim_matrix)
        t‚āźŠĶ¶_basis_reim_matrix =
            reduce(hcat, t‚āźŠĶ¶_basis_reim; init = Matrix{Float64}(undef, N, 0))
    end
    isempty(t‚āźŠĶ¶_basis_reim_matrix) && return Vector{Vector{Float64}}()

    # ------------------------------------------------------------------------------------ #
    # Make sure we have a reasonably pretty-looking basis in the end by sparsifying and
    # dropping near-zero elements explicitly

    # convert null-space to a sparse column form
    t‚āźŠĶ¶_basis_reim_matrix_form‚Ä≤ = _poormans_sparsification(t‚āźŠĶ¶_basis_reim_matrix)
    t‚āźŠĶ¶_basis_reim = [collect(v) for v in eachcol(t‚āźŠĶ¶_basis_reim_matrix_form‚Ä≤)]

    # prune near-zero elements of basis vectors
    _prune_at_threshold!(t‚āźŠĶ¶_basis_reim)

    return t‚āźŠĶ¶_basis_reim
end

# Wrapper around `nullspace` that falls back to QR-iteration SVD if the default
# divide-and-conquer SVD (`gesdd!`) fails with a `LAPACKException`. The fallback is slower
# but unconditionally convergent, and is needed for large constraint matrices arising in
# high-symmetry space groups (e.g., SG 230) on some platforms/LAPACK implementations.
function _retrying_nullspace(
    A;
    atol::Real = 0.0,
    rtol::Real = (min(size(A)...) * eps(real(float(oneunit(eltype(A)))))) * iszero(atol)
)
    try
        return nullspace(A; atol, rtol)
    catch e
        e isa LinearAlgebra.LAPACKException || rethrow(e)
        # the below simply copies the LinearAlgebra implementation of nullspace, with the
        # only adjustment being to pass `alg = LinearAlgebra.QRIteration()` to `svd` to
        # ensure convergence; 
        # TODO: change to `nullspace(A; atol, rtol, alg = LinearAlgebra.QRIteration())`
        #       if https://github.com/JuliaLang/LinearAlgebra.jl/issues/1571 is resolved
        SVD = svd(A; full = true, alg = LinearAlgebra.QRIteration())
        tol = max(atol, SVD.S[1]*rtol)
        indstart = sum(s -> s .> tol, SVD.S) + 1
        return copy((@view SVD.Vt[indstart:end,:])')
    end
end

function _aggregate_constraints(
    Q::AbstractArray{<:Number, 4},
    Z::Union{AbstractArray{<:Number, 4}, <:Number},
    row_atol::Real = 1e-10,
)
    # store constraints in one big vector initially; then we reshape to a matrix after
    constraint_vs = Vector{promote_type(eltype(Q), eltype(Z))}()
    _aggregate_constraints!(constraint_vs, Q, Z, row_atol)

    # return a C√óJ matrix where each row is a constraint, across C constraints
    J = size(Q, 2) # number of elements per constraint
    return _reshape_aggregate_constraints(constraint_vs, J)
end

function _aggregate_constraints(
    # same as `_aggregate_constraints(Q, V)` but for a vector of Qs and Zs
    Qs::AbstractVector{<:AbstractArray{<:Number, 4}},
    Zs::AbstractVector{<:AbstractArray{<:Number, 4}},
    row_atol::Real = 1e-10,
)
    constraint_vs = Vector{promote_type(eltype(eltype(Qs)), eltype(eltype(Zs)))}()
    for (Q, Z) in zip(Qs, Zs)
        _aggregate_constraints!(constraint_vs, Q, Z, row_atol)
    end
    J = size(first(Qs), 2)
    return _reshape_aggregate_constraints(constraint_vs, J)
end

function _aggregate_constraints!( # modifies `constraint_vs` in place
    constraint_vs::Vector{<:Number},
    Q::AbstractArray{<:Number, 4},
    Z::Union{AbstractArray{<:Number, 4}, <:Number},
    row_atol::Real = 1e-10,
)
    # store constraints in one big vector initially; then we reshape to a matrix after
    J = size(Q, 2)
    c = Vector{eltype(constraint_vs)}(undef, J)

    # avoid repeated recomputation of `norm(c‚Ä≤)` in collinearity check below
    norms = if isempty(constraint_vs)
        Vector{typeof(norm(c))}()
    else # `constraint_vs` is not empty
        map(1:J:length(constraint_vs)) do n
            c‚Ä≤ = @view constraint_vs[n:n+J-1]
            norm(c‚Ä≤)
        end
    end

    # aggregate constraints into `constraint_vs`
    for i in axes(Q, 1), s in axes(Q, 3), t in axes(Q, 4)
        q = @view Q[i, :, s, t]
        z = Z isa Number ? Z : @view Z[i, :, s, t]
        c .= q .- z

        # don't add empty or near-empty constraints
        norm_c = norm(c)
        norm_c < row_atol && continue

        # check whether `c` is collinear w/ any existing constraint; if so, don't add again
        c_dot_c‚Ä≤s = reshape(constraint_vs, J, length(constraint_vs) √∑ J)' * c # elements are `dot(c, c‚Ä≤)`, with `c‚Ä≤` and existing constraint
        collinear = any(zip(c_dot_c‚Ä≤s, norms)) do (c_dot_c‚Ä≤, norm_c‚Ä≤)
            # NB: works also if zip(‚Ä¶) is empty, cf. `any("empty iterator") = false`
            abs(c_dot_c‚Ä≤) ‚Čą norm_c * norm_c‚Ä≤ # collinear if true
        end
        collinear && continue

        # add the constraint to the list
        append!(constraint_vs, c)
        append!(norms, norm_c)
    end

    return constraint_vs
end

function _reshape_aggregate_constraints(
    constraint_vs::Vector{<:Number}, # vector of inline-concatenated constraints
    J::Int, # number of elements per constraint: ‚Č° size(Mm,2) = size(Q,2) = size(Z,2)
)
    C = length(constraint_vs) √∑ J # number of constraints
    return permutedims(reshape(constraint_vs, J, C))
end

"""
    reciprocal_constraints_matrices(
        Mm::AbstractArray{Int,4}, 
        gens::AbstractVector{SymOperation{D}}, 
        h_orbit::HoppingOrbit{D}
    ) --> Vector{Array{Int,4}}

Compute the reciprocal constraints matrices for the generators of the SG. This is done by
permuting the rows of the M matrix according to the symmetry operation acting on k-space.
See more details in
`permute_symmetry_related_hoppings_under_symmetry_operation` and `devdocs.md`.
"""
function reciprocal_constraints_matrices(
    Mm::AbstractArray{Int, 4},
    gens::AbstractVector{SymOperation{D}},
    h_orbit::HoppingOrbit{D},
) where {D}
    Zs = Vector{Array{Int, 4}}(undef, length(gens))
    for (i, op) in enumerate(gens)
        Z = similar(Mm)
        PŠĶÄ = transpose(
            _permute_symmetry_related_hoppings_under_symmetry_operation(h_orbit, op),
        )
        for s in axes(Mm, 3)
            for t in axes(Mm, 4) # vŠĶÄ ő°ŠĶÄ M‚āõ‚āú t => v‚āó ő°ŠĶÄ‚āóŠĶĘ MŠĶĘ‚Īľ‚āõ‚āú t‚Īľ = v‚āó PŠĶĘ‚āó MŠĶĘ‚Īľ‚āõ‚āú t‚Īľ
                Z[:, :, s, t] .= PŠĶÄ * @view Mm[:, :, s, t] # PŠĶÄ M‚ĀĹňĘŠĶó‚Āĺ
            end
        end
        Zs[i] = Z
    end
    return Zs
end

"""
Build the P matrix for a particular symmetry operation acting on k-space, which permutes the
rows of the M matrix.

To obtain the P matrix, we exploit that the action is on exponentials of the type
``exp(2ŌÄiūĚź§‚čÖőī)``, and instead act on őī ‚ąą `h_orbit.orbit` rather than on k. Because of this,
we need to use the inverse of the rotation part of the symmetry operation.

!!! details "Sketch of proof"
    Assume g={R|ŌĄ} and `Crystalline` implements gk=(R‚ĀĽ¬Ļ)ŠĶÄk. Then
    (gk)‚čÖőī = ((R‚ĀĽ¬Ļ)ŠĶÄk)‚čÖőī + ŌĄ = k‚čÖ(R‚ĀĽ¬Ļ)őī.

!!! info
    It is assumed that the operation `op` is provided in a primitive setting.

!!! warning
    This function is an internal helper function for `reciprocal_constraints_matrices` and
    is not part of the public API.
"""
function _permute_symmetry_related_hoppings_under_symmetry_operation(
    h_orbit::HoppingOrbit{D},
    op::SymOperation{D},
) where {D}
    # P is a square matrix that acts as `op` on `v`, i.e., represents ``op ‚ąė v``, via a
    # matrix-vector product `P*v`. Equivalently, `P` can act on `M` (via its transpose)
    # for expressions of the kind (Pv)ŠĶÄ M ‚Ä¶ = vŠĶÄ PŠĶÄ M ‚Ä¶
    P = zeros(Int, length(orbit(h_orbit)), length(orbit(h_orbit)))
    for (i, őīŠĶĘ) in enumerate(orbit(h_orbit))
        # crystalline implements gk = [R‚ĀĽ¬Ļ]ŠĶÄk. However, since we don't have access to k,
        # being a symbolic variable, we will translate its action into őī. For this,
        # we implement a simple trick: ([R‚ĀĽ¬Ļ]ŠĶÄ k)¬∑r = R‚ĀĽ¬ĻŠĶĘ‚Īľ kŠĶĘ r‚Īľ = k¬∑(R‚ĀĽ¬Ļ r)
        R‚ĀĽ¬Ļ = SymOperation(inv(rotation(op))) # rotation-only part of `op`
        őīŠĶĘ‚Ä≤ = compose(R‚ĀĽ¬Ļ, őīŠĶĘ)
        j = findfirst(orbit(h_orbit)) do őī‚Ä≤‚Ä≤
            isapprox(őīŠĶĘ‚Ä≤, őī‚Ä≤‚Ä≤, nothing, false; atol = VEC_CMP_ATOL)
        end
        isnothing(j) &&
            error(lazy"hopping element $őīŠĶĘ not closed under $op in $(orbit(h_orbit))")
        P[i, j] = 1
        # since P acts as g v = P v, we have (gv)ŠĶĘ = v‚Īľ = ‚ąĎ‚āĖ PŠĶĘ‚āĖ v‚āĖ so PŠĶĘ‚āĖ = őīŠĶĘ‚Īľ
    end
    return P
end

"""
Poor man's "matrix sparsification" via the reduced row echelon form.

!!! warning
    This function is an internal helper function for `obtain_basis_free_parameters` and
    is not part of the public API.
"""
function _poormans_sparsification(
    A::AbstractMatrix{<:Number};
    rref_tol::Union{Nothing, Float64} = SPARSIFICATION_ATOL_DEFAULT,
)
    # following appendix E of the Qsymm paper (https://arxiv.org/abs/1806.08363) [copied
    # over from Neumann.jl] [copied over from MPBUtils.jl]
    if !isnothing(rref_tol)
        # use a relatively low tolerance in `rref` to avoid explosions of errors
        # NB: this optional tolerance argument of `rref!` is undocumented :(
        return transpose(rref!(copy(transpose(A)), rref_tol))
    end
    return transpose(rref(transpose(A)))
end

"""
Prune near-zero elements of vectors in `vs`.

!!! warning
    This function is an internal helper function for `obtain_basis_free_parameters` and
    is not part of the public API.
"""
function _prune_at_threshold!(
    vs::AbstractVector{<:AbstractVector{T}};
    atol::Real = PRUNE_ATOL_DEFAULT,
) where {T <: Complex}
    for v in vs
        for (j, v‚Īľ) in enumerate(v)
            r‚Īľ, i‚Īľ = reim(v‚Īľ)
            r‚Īľ‚Ä≤ = ifelse(abs(r‚Īľ) < atol, zero(real(T)), r‚Īľ)
            i‚Īľ‚Ä≤ = ifelse(abs(i‚Īľ) < atol, zero(real(T)), i‚Īľ)
            v[j] = T(r‚Īľ‚Ä≤, i‚Īľ‚Ä≤)
        end
    end
    return vs
end

function _prune_at_threshold!(
    vs::AbstractVector{<:AbstractVector{T}};
    atol::Real = PRUNE_ATOL_DEFAULT,
) where {T <: Real} # needed for type consistency in TRS implementation
    for v in vs
        for (j, v‚Īľ) in enumerate(v)
            v[j] = abs(v‚Īľ) < atol ? zero(T) : v‚Īľ
        end
    end
    return vs
end

